package BookSample;

public class Page98_Sample2 {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 3;
        int sum = num1 + num2;

        System.out.printf("변수 num1의 값은 %d입니다.%n",num1);
        System.out.printf("변수 num2의 값은 %d입니다.%n",num2);
        System.out.printf("num1+num2의 값은 %d입니다.%n",sum);

        num1 = num1 + 1;
        System.out.printf("변수 num1의 값에 1을 더하면 %d입니다.",num1);
    }
}
