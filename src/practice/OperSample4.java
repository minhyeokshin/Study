package practice;

public class OperSample4 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        System.out.println("num1과 num2로 다양한 연산을 수행합니다.");
        System.out.printf("num1+num2는 %d입니다.%n",num1+num2);
        System.out.printf("num1-num2는 %d입니다.%n",num1-num2);
        System.out.printf("num1*num2는 %d입니다.%n",num1*num2);
        System.out.printf("num1/num2는 %d입니다.%n",num1/num2);
        System.out.println("num1%num2는 " + (num1%num2) + "m입니다.");
    }
}
